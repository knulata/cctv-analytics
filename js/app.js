// Load WhatsApp status in sidebar
async function loadWAStatus() {
    try {
        const res = await fetch('/api/whatsapp/status');
        const data = await res.json();
        const dot = document.getElementById('wa-dot');
        const label = document.getElementById('wa-label');
        if (dot && label) {
            dot.className = 'wa-dot ' + (data.enabled ? 'on' : 'off');
            label.textContent = data.enabled ? `WA: ${data.number}` : 'WA: Not configured';
        }
    } catch(e) {}
}

// Load alert count for nav badge
async function loadAlertBadge() {
    try {
        const res = await fetch('/api/analytics/summary');
        const data = await res.json();
        const badge = document.getElementById('alert-badge');
        if (badge) {
            if (data.total_alerts > 0) {
                badge.textContent = data.total_alerts;
                badge.style.display = 'block';
            } else {
                badge.style.display = 'none';
            }
        }
    } catch(e) {}
}

document.addEventListener('DOMContentLoaded', () => {
    loadWAStatus();
    loadAlertBadge();
});
