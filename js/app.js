/* Shared utilities for CCTV Analytics */

function timeAgo(ts) {
    if (!ts) return 'never';
    const diff = (Date.now() - new Date(ts).getTime()) / 1000;
    if (diff < 0) return 'just now';
    if (diff < 60) return 'just now';
    if (diff < 3600) return Math.floor(diff / 60) + 'm ago';
    if (diff < 86400) return Math.floor(diff / 3600) + 'h ago';
    return Math.floor(diff / 86400) + 'd ago';
}

function scoreClass(val, invert = false) {
    if (invert) return val <= 3 ? 'good' : val <= 6 ? 'ok' : 'bad';
    return val >= 7 ? 'good' : val >= 4 ? 'ok' : 'bad';
}

function statColor(val, invert = false) {
    if (invert) return val <= 3 ? 'green' : val <= 6 ? 'yellow' : 'red';
    return val >= 7 ? 'green' : val >= 4 ? 'yellow' : 'red';
}

